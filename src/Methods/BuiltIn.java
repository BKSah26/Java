package Methods;

public class BuiltIn {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(10));
        System.out.println(Math.cbrt(100));
        System.out.println(Math.PI);
        //Modulus Function
        System.out.println(Math.abs(-88));
        //Greatest Integer Function
        System.out.println(Math.floor(-7.8));
        System.out.println(Math.min(3, 9));
        System.out.println(Math.max(2.9, 2));
        System.out.println(Math.pow(2, 5));
        System.out.println(Math.pow(3.14, 2.69));
        System.out.println(Math.pow(-3.6125, -2));
        System.out.println(Math.pow(-7.777, -23.36));
    }
}
