package Basics;

public class Variables_3 {
    public static void main(String[] args) {
        int x=5;
        System.out.println("Value of x before modification: "+x);
        x=x+2;
        System.out.println("Value of x after modification: "+x);
        int y=x*5;
        System.out.println("Value of y: "+y);
    }
}
