package Basics;

public class Variables_2 {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x=20;
        System.out.println(x);
        x=x+5;
        System.out.println(x);
        x=x-10;
        System.out.println(x);
        x=x*2;
        System.out.println(x);
        x=x/3;
        System.out.println(x);
    }
}
