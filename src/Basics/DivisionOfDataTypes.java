package Basics;

public class DivisionOfDataTypes {
    public static void main(String[] args){
        double a=5/2;
        System.out.println(a);

        double b=5.0/2.0;
        System.out.println(b);

        double c=5/2.0;
        System.out.println(c);

        double d=5.0/2.0;
        System.out.println(d);
    }
}
